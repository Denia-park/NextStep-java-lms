create table course (
    id bigint generated by default as identity,
    generation int not null,
    title varchar(255) not null,
    creator_id bigint not null,
    created_at timestamp not null,
    updated_at timestamp,
    primary key (id)
);

create table session
(
    id                bigint generated by default as identity,
    title             varchar(255) not null,
    price             bigint       not null,
    start_date        timestamp    not null,
    end_date          timestamp    not null,
    session_status    varchar(50)  not null,
    recruiting_status varchar(50)  not null,
    max_student_limit int          not null,
    course_id         bigint       not null,
    creator_id        bigint       not null,
    created_at        timestamp    not null,
    updater_id        bigint,
    updated_at        timestamp,
    primary key (id),
    foreign key (course_id) references course (id)
);

create table cover_image (
    id         bigint generated by default as identity,
    file_size  int         not null,
    width      int         not null,
    height     int         not null,
    image_type varchar(50) not null,
    session_id bigint      not null,
    primary key (id),
    foreign key (session_id) references session (id)
);

create table ns_user (
    id bigint generated by default as identity,
    user_id varchar(20) not null,
    password varchar(20) not null,
    name varchar(20) not null,
    email varchar(50),
    created_at timestamp not null,
    updated_at timestamp,
    primary key (id)
);

create table session_user_list (
    user_id    bigint not null,
    session_id bigint not null,
    primary key (user_id, session_id),
    foreign key (user_id) references ns_user (id),
    foreign key (session_id) references session (id)
);

create table session_selection_user_list (
    user_id    bigint not null,
    session_id bigint not null,
    select_status varchar(50) not null,
    primary key (user_id, session_id),
    foreign key (user_id) references ns_user (id),
    foreign key (session_id) references session (id)
);

create table question (
    id bigint generated by default as identity,
    created_at timestamp not null,
    updated_at timestamp,
    contents clob,
    deleted boolean not null,
    title varchar(100) not null,
    writer_id bigint,
    primary key (id)
);

create table answer (
    id bigint generated by default as identity,
    created_at timestamp not null,
    updated_at timestamp,
    contents clob,
    deleted boolean not null,
    question_id bigint,
    writer_id bigint,
    primary key (id)
);

create table delete_history (
    id bigint not null,
    content_id bigint,
    content_type varchar(255),
    created_date timestamp,
    deleted_by_id bigint,
    primary key (id)
);
